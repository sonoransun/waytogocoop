# Lighting tuned to make the moire ridges and Cooper-pair isosurface readable
# without over-specular highlights. Applied via ``go.Surface(lighting=...)``.
LIGHTING_PRESET: Final[dict[str, float]] = dict(
    ambient=0.4,
    diffuse=0.8,
    specular=0.3,
    roughness=0.5,
    fresnel=0.2,
)

# Light position is in scene-data units, not normalised — Plotly's convention.
LIGHT_POSITION: Final[dict[str, int]] = dict(x=100, y=200, z=0)

# Named iso-style camera used for consistent screenshots; moved here from
# figure_factory._scene_camera_presets() so the script capture path and the
# Dash pages share one definition.
CAMERA_PRESETS: Final[dict[str, dict]] = {
    "iso":  dict(eye=dict(x=1.5, y=1.5, z=1.0)),
    "top":  dict(eye=dict(x=0.0, y=0.0, z=2.5), up=dict(x=0, y=1, z=0)),
    "side": dict(eye=dict(x=2.5, y=0.0, z=0.2)),
}


